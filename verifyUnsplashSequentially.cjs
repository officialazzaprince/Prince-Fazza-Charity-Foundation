const uniqueCandidates = [...new Set(candidates)];
const valid = [];

async function checkUrl(id) {
  return new Promise((resolve) => {
    https.get(`https://images.unsplash.com/photo-${id}?w=10`, (res) => {
      resolve({ id, status: res.statusCode });
    }).on('error', () => {
      resolve({ id, status: 0 });
    });
  });
}

(async () => {
  for (const id of uniqueCandidates) {
    const res = await checkUrl(id);
    if (res.status === 200) {
      valid.push(id);
    }
  }
  const fs = require('fs');
  fs.writeFileSync('valid_ids.json', JSON.stringify(valid, null, 2));
  console.log(`Found ${valid.length} valid images out of ${uniqueCandidates.length}. Saved to valid_ids.json`);
})();
