// Auto-fallback translator function that replaces dynamic names and structural words 
// to render clean translations for any of the 20 global languages.
export function translate(key: string, currentLang: string): string {
  const currentDict = DICTIONARY[currentLang] || DICTIONARY["en"];
  if (currentDict && currentDict[key]) {
    return currentDict[key];
  }
  
  // Safe Fallback to English dictionary
  if (DICTIONARY["en"] && DICTIONARY["en"][key]) {
    return DICTIONARY["en"][key];
  }
  
  // Return the raw key capitalized formatted if no dictionary exists
  return key.replace(/_/g, " ").replace(/\b\w/g, c => c.toUpperCase());
}

